#include <stdio.h>

int main() {
	# declaring some int variables 
	int a, b, c;

	# assigning values to a and b
	a = 5;
	b = 7;
	
	# the value of c will be the result adding a and b
	c = a + b;

	# print the result
	printf("%d + %d = %d\n", a, b, c);

	# return the value 0 because the program has run sucessfully
	return 0;
}
